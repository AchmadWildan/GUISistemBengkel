    public ArrayList<Akun> akunList() {
        ArrayList<Akun> akunList = new ArrayList<>();
        try {
            query = "SELECT * FROM " + nameTabel;
            stat = conn.createStatement();
            rs = stat.executeQuery(query);
            Akun akun;
            while (rs.next()) {
                if (rs.getString("Deleted").replaceAll("\\s+", "").equals("0")) {
                    akun = new Akun(rs.getString("IDStaff").replaceAll("\\s+", ""), rs.getString("NamaStaff").replaceAll("\\s+", ""));
                    akunList.add(akun);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return akunList;
    }

    public void tampil_akun() {
        ArrayList<Akun> list = akunList();
        DefaultTableModel model = (DefaultTableModel) tb_akun.getModel();
        Object[] row = new Object[2];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getIDStaff();
            row[1] = list.get(i).getNamaStaff();
            model.addRow(row);
        }
    }

