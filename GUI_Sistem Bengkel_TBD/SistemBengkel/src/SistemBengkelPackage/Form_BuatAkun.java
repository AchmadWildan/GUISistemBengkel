    private void tb_akunMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_akunMouseClicked
        tb_akun.setSelectionForeground(Color.GREEN);
        try {
            int baris = tb_akun.rowAtPoint(evt.getPoint());
            String idstaff = tb_akun.getValueAt(baris, 0).toString();
            query = "SELECT * FROM " + nameTabel + " WHERE IDStaff='" + idstaff.replaceAll("\\s+", "") + "'";
            stat = conn.createStatement();
            rs = stat.executeQuery(query);

            String namastaff = "", alamat = "", posisi = "", pw = "";

            if (rs.next()) {
                namastaff = String.valueOf(rs.getString("NamaStaff")).replaceAll("null", "").replaceAll("\\s+", "");
                alamat = String.valueOf(rs.getString("Alamat")).replaceAll("null", "").replaceAll("\\s+", "");
                posisi = String.valueOf(rs.getString("Posisi")).replaceAll("null", "").replaceAll("\\s+", "");
                pw = String.valueOf(rs.getString("Password")).replaceAll("null", "").replaceAll("\\s+", "");
            }

            txt_idstaff.setText(idstaff);
            txt_namastaff.setText(namastaff);
            txt_area_alamat.setText(alamat);
            txt_caridata.setText(posisi);
            txt_pass.setText(pw);
            txt_konfir_pass.setText(pw);

        } catch (SQLException ex) {
            Logger.getLogger(Form_BuatAkun.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tb_akunMouseClicked

