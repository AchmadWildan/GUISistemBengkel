    private static class Akun {

        private String IDStaff, NamaStaff;

        public Akun(String IDStaff, String NamaStaff) {
            this.IDStaff = IDStaff;
            this.NamaStaff = NamaStaff;
        }

        public String getIDStaff() {
            return IDStaff;
        }

        public String getNamaStaff() {
            return NamaStaff;
        }

    }

    private void CariData(String cd) throws SQLException {
        query = "SELECT * FROM " + nameTabel + " WHERE IDStaff like '" + cd + "%'";
        stat = conn.createStatement();
        rs = stat.executeQuery(query);
        if (rs.next()) {
            ArrayList<Akun> akunList = new ArrayList<>();
            try {
                Akun akun;
                if (rs.getString("Deleted").replaceAll("\\s+", "").equals("0")) {
                    akun = new Akun(rs.getString("IDStaff").replaceAll("\\s+", ""), rs.getString("NamaStaff").replaceAll("\\s+", ""));
                    akunList.add(akun);
                }
                while (rs.next()) {
                    if (rs.getString("Deleted").replaceAll("\\s+", "").equals("0")) {
                        akun = new Akun(rs.getString("IDStaff").replaceAll("\\s+", ""), rs.getString("NamaStaff").replaceAll("\\s+", ""));
                        akunList.add(akun);
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

            DefaultTableModel model = (DefaultTableModel) tb_akun.getModel();
            model.setRowCount(0);
            Object[] row = new Object[2];
            for (Iterator<Akun> it = akunList.iterator(); it.hasNext();) {
                Akun akunList1 = it.next();
                row[0] = akunList1.getIDStaff();
                row[1] = akunList1.getNamaStaff();
                model.addRow(row);
            }
        }
    }
