    private void click_edit(String idstaff) throws SQLException, Exception {
        if (!(txt_pass.getText().equals(txt_konfir_pass.getText()))) {
            throw new Exception("Password Harus Sama");
        }

        query = "UPDATE " + nameTabel + " SET IDStaff=?, NamaStaff=?, Alamat=?, Posisi=?, Keterangan=?, Deleted=?,Password=? WHERE IDStaff = '" + idstaff.replaceAll("\\s+", "") + "'";
        PreparedStatement pst = conn.prepareStatement(query);
        pst.setString(1, idstaff);
        pst.setString(2, txt_namastaff.getText());
        pst.setString(3, txt_area_alamat.getText());
        pst.setString(4, txt_posisi.getText());
        pst.setString(5, "-");
        pst.setString(6, "0");
        pst.setString(7, txt_pass.getText());
        pst.executeUpdate();
        txt_idstaff.setText("");
        txt_namastaff.setText("");
        txt_area_alamat.setText("");
        txt_caridata.setText("");
        txt_pass.setText("");
    }

    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        try {
            if (!(txt_pass.getText().equals(txt_konfir_pass.getText()))) {
                throw new Exception("Password Harus Sama");
            }

            query = "SELECT * FROM " + nameTabel + " WHERE IDStaff='" + txt_idstaff.getText().replaceAll("\\s+", "") + "'";
            
            stat = conn.createStatement();
            rs = stat.executeQuery(query);
            if (rs.next()) {
                if (rs.getString("Deleted").replaceAll("\\s+", "").equals("1")) {
                    click_edit(txt_idstaff.getText());
                    tampil_akun();
                } else {
                    throw new Exception("Akun Sudah Ada\nKlik data lalu tekan edit jika ingin merubah data");
                }
            } else {
                if (!txt_idstaff.getText().replaceAll("\\s+", "").equals("")) {
                    query = "INSERT INTO " + nameTabel + " (IDStaff,NamaStaff,Alamat,Posisi,Keterangan,Deleted,Password) VALUES (?,?,?,?,?,?,?)";
                    PreparedStatement pst = conn.prepareStatement(query);
                    pst.setString(1, txt_idstaff.getText());
                    pst.setString(2, txt_namastaff.getText());
                    pst.setString(3, txt_area_alamat.getText());
                    pst.setString(4, txt_caridata.getText());
                    pst.setString(5, "-");
                    pst.setString(6, "0");
                    pst.setString(7, txt_pass.getText());
                    pst.executeUpdate();
                } else {
                    throw new Exception("Data Tidak Boleh Kosong");
                }
            }
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            DefaultTableModel model = (DefaultTableModel) tb_akun.getModel();
            model.setRowCount(0);
            tampil_akun();
            txt_idstaff.setText("");
            txt_namastaff.setText("");
            txt_area_alamat.setText("");
            txt_caridata.setText("");
            txt_pass.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Penyimpanan Data Gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_saveActionPerformed

